describe 'A game of tennis' do
  context 'when player 1 has 4 points' do
    context 'and leads the opponent by 2 points' do
      it 'confirms player 1 as the winner'
    end
  
    context 'and leads the opponent by > 2 points' do
      it 'confirms player 1 as the winner'
    end
  end

  context 'when player 1 has > 4 points' do
    context 'and leads the opponent by > 2 points' do
      it 'confirms player 1 as the winner'
    end
  end
end